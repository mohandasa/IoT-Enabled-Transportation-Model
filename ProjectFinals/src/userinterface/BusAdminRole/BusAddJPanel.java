    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int selectedRow=BusInfojTable.getSelectedRow();
        if(selectedRow>=0)
        {
            int dialogResult=JOptionPane.showConfirmDialog(null, "Please confirm whether to delete","Confirmation",JOptionPane.INFORMATION_MESSAGE);
            if(dialogResult==JOptionPane.YES_OPTION);
            {
                Bus b=(Bus) BusInfojTable.getValueAt(selectedRow,0);
                busDirectory.removeBus(b);
                refreshTable();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select Bus to delete", "delete account", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


