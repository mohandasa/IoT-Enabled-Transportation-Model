     public void refreshTable()
    {
        DefaultTableModel dtm=(DefaultTableModel)BusInfojTable.getModel();
        dtm.setRowCount(0);
        for(Bus b:busDirectory.getBusList())
        {
            Object[] row=new Object[3];
            row[0]=b;
            row[1]=b.getBusSource();
            row[2]=b.getBusDestination();
            
            dtm.addRow(row);
            
        }
    }
    
    
