    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        String message = messageJTextField.getText();
         int selectedRow = BusInfojTable.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }

        BusAdminWorkRequest request = new BusAdminWorkRequest();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setStatus("Sent");
        //request.setBus(String.valueOf(selectedRow));
        Bus b = (Bus) BusInfojTable.getValueAt(selectedRow, 0);
        System.out.println(b.getBusSource()+"------------");
        request.setBus(b);
        
        Enterprise ent=null;

        for (Network network: business.getNetworkList()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprise instanceof BusTransportEnterprise){
                    ent = enterprise;
                }
            }
        }

        Organization org = null;
        // Enterprise ent = (PassengerAdminEnterprise)enterprise;

        for (Organization organization : ent.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof BusDriverOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
        JOptionPane.showMessageDialog(null,"Bus Request added","Information",JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_requestTestJButtonActionPerformed

